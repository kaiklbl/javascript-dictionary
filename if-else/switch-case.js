switch(new Date().getDay()){
    case 0:
        alert('Heute ist Sonntag');
        break;
    case 1:
        alert('Heute ist Montag');
        break;
    case 2:
        alert('Heute ist Dienstag');
        break;
    case 3:
        alert('Heute ist Mittwoch');
        break;
    case 4:
        alert('Heute ist Donerstag');
        break;
    case 5:
        alert('Heute ist Freitag');
        break;
    case 6:
        alert('Heute ist Samstag');
        break;
}